#include <bits/stdc++.h>
using namespace std;
int main()
{
#ifndef ONLINE_JUDGE
    freopen("input.txt","r",stdin);
    freopen("output.txt","w",stdout);
#endif

    // subsets of array with xor
    int n;
    cin>>n;
    int a[n];
    for (int i = 0; i < n; i++) cin>>a[i];

    for(int mask=0;mask<(1<<n);mask++){
        for (int bit = 0; bit < n; bit++)
        {
            if((mask>>bit)&1) cout<<a[bit]<<" ";
        }
        cout<<endl;   
    }
}